	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{	
		Log::info(Input::all());
		$title = Input::get('title');
		$body = Input::get('body');
		$post = new Post();
		$post->title = $title;
		$post->body = $body;
		$post->save();
		
		return Redirect::action('PostsController@index');
	}

